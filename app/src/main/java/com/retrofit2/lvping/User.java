package com.retrofit2.lvping;

public class User {


    /**
     * uuId : 0
     * currGold : 0
     * totalGold : 0
     * realGold : 0
     */

    private int uuId;
    private int currGold;
    private int totalGold;
    private int realGold;

    public int getUuId() {
        return uuId;
    }

    public void setUuId(int uuId) {
        this.uuId = uuId;
    }

    public int getCurrGold() {
        return currGold;
    }

    public void setCurrGold(int currGold) {
        this.currGold = currGold;
    }

    public int getTotalGold() {
        return totalGold;
    }

    public void setTotalGold(int totalGold) {
        this.totalGold = totalGold;
    }

    public int getRealGold() {
        return realGold;
    }

    public void setRealGold(int realGold) {
        this.realGold = realGold;
    }
}
